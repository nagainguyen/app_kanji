    void luuVaoSQL(){
        if(TaiKhoan != null){
            CauLenh connect = new CauLenh();
            var conn = connect.conn;
            try{
                boolean kt = false;
                var cauLenh = conn.prepareStatement("select * from DiemSo where TaiKhoan = '" + TaiKhoan + "'");
                var thucThi = cauLenh.executeQuery();
                List<DiemSo> list = new ArrayList();
                while(thucThi.next()){
                    DiemSo a = new DiemSo(thucThi.getString(1), thucThi.getString(2), thucThi.getInt(3),thucThi.getInt(4));
                    list.add(a);
                }
                if (list == null){
                    String lenh = "insert into DiemSo VALUES ('" + TaiKhoan + "','N" + trinhDo + "'," + diemSo + "," + soCau + ")";
                    var Statement = conn.createStatement();
                    Statement.executeUpdate(lenh);
                }
                else{
                    for(DiemSo a : list){
                        if(a.getTrinhDo().trim().equals("N"+trinhDo)){
                            if(diemSo > a.getDiem()){
                                String lenh = "UPDATE DiemSo SET Diem = "+ diemSo +" WHERE TaiKhoan = '"+ TaiKhoan +"' and TrinhDo = 'N"+ trinhDo +"'";
                                var Statement = conn.createStatement();
                                Statement.executeUpdate(lenh);
                            }
                            kt = true;
                            break;
                        }
                    }
                    if(kt == false){
                        String lenh = "insert into DiemSo VALUES ('" + TaiKhoan + "','N" + trinhDo + "'," + diemSo + "," + soCau + ")";
                        var Statement = conn.createStatement();
                        Statement.executeUpdate(lenh);
                    }
                }
            }
            catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
    }
    
