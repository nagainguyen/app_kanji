    private void countdonw() {
        count = 1;
        timer = new Timer(0, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (count < 0) {
                    timer.stop();
                    label_KetQua.setText("Câu đúng/Tổng: " + diemSo + "/" + soCau);
                    label_TrinhDo.setText("Trình độ: N" + trinhDo);
                } else {
                    count--;
                }
            }
        });
        if (count != 0) {
            timer.start();
        }
    }
    
